package com.loop;

public class For_Loop_Task {
	
	public static void main(String[] args) {
		
		System.out.println("Task1:");
		System.out.println("start");
		for (int i = 99; i >=90; i--) {
			System.out.print(i+" ");
		}
		System.out.println();
		System.out.println("end");
		
		System.out.println();
		
		System.out.println("Task2:");
		System.out.println("start");
		for (int i = 1; i <=3; i++) {
			System.out.print("A ");
		}
		System.out.println();
		System.out.println("end");
		
		System.out.println();
		
		System.out.println("Task3:");
		for (int i = 1; i <= 3; i++) {
			System.out.println("start");
			System.out.println(i);
		}
		System.out.println("end");
		
	}

}
